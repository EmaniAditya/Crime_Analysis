function handleFileSelect(evt) {
    var file = evt.target.files[0];
    console.log(file);
    Papa.parse(file, {
        header: true,
        dynamicTyping: true,
        complete: function(results) {
            var data = results.data;
            var html = '<table>';
            html += '<thead><tr>';
            Object.keys(data[0]).forEach(function(key) {
                html += '<th>' + key + '</th>';
            });
            html += '</tr></thead>';
            html += '<tbody>';
            data.forEach(function(row1) {
                html += '<tr>';
                Object.values(row1).forEach(function(value) {
                    html += '<td>' + value + '</td>';
                });
                html += '</tr>';
            });
            html += '</tbody></table>';
            document.getElementById('csv-data').innerHTML = html;
        }

    });
}

function changecolor() {
    const text1 = document.getElementById("cluster-text");
    const text = text1.textContent;
    let color = '#ffa500';


    if (text === "RED ZONE") {
        color = "red";
    } else if (text === "GREEN ZONE") {
        color = "green"
    } else {
        color = "#ffa500";
    }
    text1.style.color = color;


}